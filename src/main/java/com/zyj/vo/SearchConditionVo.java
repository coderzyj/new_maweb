package com.zyj.vo;

public class SearchConditionVo {
	private String location;
	private String deloc;
	private String pricearea;
	private String housekind;
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getDeloc() {
		return deloc;
	}
	public void setDeloc(String deloc) {
		this.deloc = deloc;
	}
	public String getPricearea() {
		return pricearea;
	}
	public void setPricearea(String pricearea) {
		this.pricearea = pricearea;
	}
	public String getHousekind() {
		return housekind;
	}
	public void setHousekind(String housekind) {
		this.housekind = housekind;
	}
	
	

}
